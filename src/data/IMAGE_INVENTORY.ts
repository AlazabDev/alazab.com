// دالة للحصول على معلومات الصورة
export function getImageInfo(id: string) {
  for (const source of Object.values(imageInventory)) {
    if (typeof source === 'object' && 'images' in source && Array.isArray(source.images)) {
      const found = source.images.find((img: any) => img.id === id);
      if (found) return found;
    }
  }
  return null;
}

// دالة للحصول على جميع الصور بالفئة
export function getImagesByCategory(category: string) {
  const results = [];
  for (const source of Object.values(imageInventory)) {
    if (typeof source === 'object' && 'images' in source && Array.isArray(source.images)) {
      const filtered = source.images.filter((img: any) => img.category === category);
      results.push(...filtered);
    }
  }
  return results;
}

